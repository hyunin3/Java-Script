const numbers = [1, 2, 3, 4, 5]

// array.reverse()
numbers.reverse()
console.log(numbers)

// array.push(values) array.pop()
numbers.push(100)
console.log(numbers)

numbers.pop()
console.log(numbers)

// array.includes(values)
console.log(numbers.includes(1))
console.log(numbers.includes(100))

// array.indexOf(value)
console.log(numbers.indexOf(3))
console.log(numbers.indexOf(100))
